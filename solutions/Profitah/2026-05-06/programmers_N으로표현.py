"""
이 문제는 
숫자 N을 최대 8번까지 사용하여 
사칙연산으로 목표 숫자 number를 만드는 최소 횟수를 구하는 문제임.

///
ex) N=5, number=12
- 5를 1번 사용: {5}
- 5를 2번 사용: {55, 10, 0, 25} // (5+5, 5-5, 5×5, 5÷5, 55)
- 5를 3번 사용: {555, 60, 15, 50, 30, 20, 125, 5, 11, ...} // (55+5, 55-5, 55×5, 55÷5, 10+5, 10-5, 10×5, 10÷5, 0+5, 0-5, 0×5, 0÷5, 25+5, 25-5, 25×5, 25÷5)
- 5를 4번 사용: {...12...} // (555+5, 555-5, 555×5, 555÷5, 60+5, 60-5, 60×5, 60÷5, 15+5, 15-5, 15×5, 15÷5, ...)
///

숫자 N을 i번 사용해서 사칙연산으로 만들 수 있는 모든 값을 dp[i]에 저장
dp[j]( N을 j번 사용해서 만들 수 있는 값들 중 하나 ) 와 dp[i-j]( N을 (i-j)번 사용해서 만들 수 있는 값들 중 하나 )의 모든 조합으로 사칙연산 수행

"""

def solution(N, number):
    # dp[i] : N을 i번 사용해서 만들 수 있는 모든 값들의 집합
    dp = [set() for _ in range(9)]  
    
    # N을 최대 8번까지 사용 (문제 조건)
    for i in range(1, 9):

        # N을 i번 이어붙인 값 추가 (ex: 5 → 55 → 555)
        # 문자열로 변환 후 곱해서 이어붙이고 다시 정수로 변환
        dp[i].add(int(str(N) * i))
        
        # j를 1부터 i-1(사용 횟수)까지 반복하며 dp[j]와 dp[i-j]를 조합
        for j in range(1, i):  

            # dp[j] : N을 j번 사용해서 만들 수 있는 값들 중 하나 선택
            for a in dp[j]:  

                # dp[i-j] : N을 (i-j)번 사용해서 만들 수 있는 값들 중 하나 선택
                for b in dp[i - j]:  

                    # 두 값을 이용해 사칙연산 결과 생성 → dp[i]에 추가
                    dp[i].add(a + b)
                    dp[i].add(a - b)
                    dp[i].add(a * b)

                    # 0으로 나누는 경우 방지 후 나눗셈 추가
                    if b != 0:  
                        dp[i].add(a // b)
        
        # 현재 i번 사용해서 목표값을 만들 수 있으면 최소 횟수이므로 바로 반환
        if number in dp[i]:  
            return i  
    
    # 1~8번까지 모두 시도했지만 만들 수 없는 경우
    return -1