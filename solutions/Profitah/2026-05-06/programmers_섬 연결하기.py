"""
이 문제는 섬들을 연결하는 최소 비용을 구하는 문제.

사용 알고리즘
클루스칼 알고리즘
최소 신장 트리를 찾는 알고리즘 중 하나로, 
간선의 가중치가 낮은 순서대로 선택하여 트리를 구성.

가중치가 낮은 간선부터 선택하여 트리를 구성하기 때문에,
최종적으로 모든 섬을 연결하는 최소 비용을 보장할 수 있음.

---

풀이흐름
1. 간선 리스트 정렬: 비용(cost)을 기준으로 오름차순 정렬
2. Union-Find 자료구조 초기화: 각 노드의 부모를 자기 자신으로 설정하여 초기에는 모든 노드가 독립된 집합이 되도록 함
3. 간선 선택: 정렬된 간선 리스트를 순회하면서, 각 간선의 양 끝 노드가 서로 다른 집합에 속하는 경우(=사이클이 발생하지 않는 경우) 해당 간선을 선택하여 두 집합을 합침
4. 비용 누적: 간선을 선택할 때마다 해당 간선의 비용을 누적하여 최종적으로 모든 섬을 연결하는 최소 비용을 계산   

"""


def solution(n, costs):
    # ** 간선을 비용 기준 오름차순 정렬 (크루스칼 핵심: 항상 가장 저렴한 간선부터 선택)
    costs.sort(key=lambda x: x[2])
    
    # Union-Find 자료구조 초기화
    # ** 각 노드의 부모를 자기 자신으로 설정.
    parent = list(range(n))
    
    def find(x):
        # 경로 압축(Path Compression): 루트까지 재귀 탐색하면서
        # 거치는 모든 노드의 부모를 루트로 직접 연결 → 이후 탐색 속도 향상
        if parent[x] != x:
            parent[x] = find(parent[x])
        return parent[x]
    
    def union(a, b):
        # 두 노드의 루트를 각각 찾아
        a = find(a)
        b = find(b)
        # ** 루트가 다를 때만 합침 (같으면 이미 같은 집합 → 연결하면 사이클 발생)
        if a != b:
            parent[b] = a  # b의 루트를 a의 루트 아래로 편입
            return True    # 합침 성공 → 이 간선을 최소신장트리에 포함
        return False       # 합침 실패 → 사이클이므로 이 간선은 스킵
    
    answer = 0
    
    for a, b, cost in costs:
        # union 성공(=새로운 연결) 시에만 비용 누적
        # union 실패(=사이클)면 해당 간선은 최소신장트리에 포함하지 않음
        if union(a, b):
            answer += cost
    
    # ** 모든 섬을 연결하는 최소 신장 트리(MST)의 총 비용 반환
    return answer