"""
삼각형에서 바로 위 두 값 중 큰 것만 선택해
누적합을 구하자.
    5
   7 3
  2 4 6
  
 가 있을 경우
 
 두번째줄은
 7+5 = 12
 3+5 = 8
 
 따라서 
 5
12 8
2 4 6

 이되며
 
 세번째줄은
 
12 + 2 = 14
max(12, 8) = 12 12+4 = 16
8 + 6 = 14
 
 이 된다.

"""


n = int(input())  # 삼각형의 높이

d = []  # 삼각형 데이터를 저장할 리스트

# 삼각형 입력 받기
for i in range(n):
    d.append(list(map(int, input().split())))

# 두 번째 줄부터 계산 시작
for i in range(1, n):

    # 현재 행의 모든 원소 확인
    for j in range(len(d[i])):

        # 맨 왼쪽 값
        # 위에서 바로 내려오는 경우만 존재
        if j == 0:
            d[i][j] = d[i][j] + d[i-1][j]

        # 맨 오른쪽 값
        # 왼쪽 위에서만 내려올 수 있음
        elif j == len(d[i]) - 1:
            d[i][j] = d[i][j] + d[i-1][j-1]

        # 가운데 값
        # 왼쪽 위 / 오른쪽 위 둘 중 큰 값 선택
        else:
            d[i][j] = max(d[i-1][j-1], d[i-1][j]) + d[i][j]

# 마지막 줄에는 각 위치까지의 최대 합이 저장됨
# 그 중 가장 큰 값이 정답
print(max(d[n-1]))