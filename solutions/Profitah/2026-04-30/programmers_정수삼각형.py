"""
구해야할 것 : 삼각형의 최대 경로 합

문제 : 
정수를 입력 받아 삼각형을 만든다.
삼각형의 꼭대기에서부터 시작하여, 
바로 아래 혹은 가장 아래의 우측 으로 내려가는데
내려가는 경로의 합이 최대가 되는 경로의 합을 구하라...


풀이흐름 : 

매 칸마다 최선의 선택만 남기고 나머지는 버리면서 내려가다가, 마지막 행에서 제일 큰 값을 뽑자


어 머임 이거 어디서 풀어봄.... 어디징!!!

---

풀이 : DP (점화식) - 바텀업
dp[i][j] = max(dp[i-1][j-1], dp[i-1][j]) + triangle[i][j]

i는 행 번호, j는 열 번호
dp[i][j]는 삼각형의 i행 j열에 도달하는 최대 경로 합을 의미한다.
점화식은 현재 칸에 도달하는 두 경로(왼쪽 위, 오른쪽 위) 중 더 큰 값에 현재 칸의 값을 더한 것이다.
첫 번째 행은 초기값(꼭대기)이고, 두 번째 행부터 점화식을 적용해
마지막 행에서 최대값을 찾아 반환한다.
"""


def solution(triangle):
    n = len(triangle)  # 삼각형의 높이
    d = triangle       # 입력 삼각형을 dp 테이블로 사용

    for i in range(1, n):          # 두 번째 줄부터 순회
        for j in range(len(d[i])): # 현재 행의 모든 원소 확인

            if j == 0:             # 맨 왼쪽 → 위에서 바로 내려오는 경우만 존재
                d[i][j] = d[i][j] + d[i-1][j]

            elif j == len(d[i]) - 1:  # 맨 오른쪽 → 왼쪽 위에서만 내려올 수 있음
                d[i][j] = d[i][j] + d[i-1][j-1]

            else:                  # 가운데 → 왼쪽 위 / 오른쪽 위 둘 중 큰 값 선택
                d[i][j] = max(d[i-1][j-1], d[i-1][j]) + d[i][j]

    return max(d[n-1])  # 마지막 행에서 최대값 반환