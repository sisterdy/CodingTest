"""
완전탐색, 리스트를 튜플로 변환 (순회해야하므로)

- 던전의 모든 순서를 permutations로 생성한다.
- 각 순서에 대해:
    1. 현재 피로도(p)를 k로 초기화
    2. 순서대로 던전을 방문
    3. 최소 필요 피로도(d[0])를 만족하면:
         - 피로도 차감 (p -= d[1])
         - 탐험 횟수 증가
    4. 만족하지 못하면 해당 순서는 종료
- 모든 경우 중 최대 탐험 횟수를 answer에 저장
- 최대값이 던전 개수와 같으면 더 볼 필요 없으므로 종료

→ 결국 "모든 순서를 시도해보고 가장 많이 방문할 수 있는 경우"를 찾는 문제.
"""
from itertools import permutations  # 던전 방문 순서를 만들기 위해 순열 사용

def solution(k, dungeons):
    answer = -1  # 최대로 탐험 가능한 던전 수를 저장할 변수

    # 던전들의 모든 방문 순서를 생성 (완전탐색)
    for case in permutations(dungeons):
        t = 0          # 현재 순서에서 탐험한 던전 수
        p = k          # 현재 남은 피로도 (초기값 k)

        # 현재 순서대로 던전 탐험 시도
        for d in case:
            # d[0] = 최소 필요 피로도, d[1] = 소모 피로도
            if d[0] <= p:      # 현재 피로도로 입장 가능하면
                p -= d[1]      # 피로도 차감
                t += 1         # 탐험 횟수 증가
            else:
                break          # 입장 불가하면 해당 순서 탐험 종료

        # 지금까지의 최대 탐험 횟수와 비교하여 갱신
        answer = max(answer, t)

        # 이미 모든 던전을 탐험했다면 더 볼 필요 없이 종료
        if answer == len(dungeons):
            break

    return answer  # 최대 탐험 가능 던전 수 반환
