"""
스택 그리디

어떤 경우에도 현재 자리에서 만들 수 있는 가장 큰 값을 선택해야 하므로
후입선출(LIFO) 구조인 스택이 적합하다.

앞의 숫자가 뒤의 숫자보다 작다면,
그 작은 숫자를 제거하는 것이 전체 숫자를 크게 만드는 최적 선택이다.

즉, 현재 숫자보다 작은 이전 숫자를 k가 허용하는 범위 내에서 제거하는
그리디 알고리즘 문제이다.

1. number를 왼쪽부터 순회
2. 현재 숫자(num)가 스택의 top보다 크면
   → k가 남아있는 동안 이전 작은 숫자를 제거
3. 제거가 끝나면 현재 숫자를 스택에 추가
4. 순회 종료 후 k가 남아있다면 뒤에서부터 제거
5. 스택을 문자열로 합쳐 최종 결과 반환

전형적인 단조 감소 스택(Monotonic Stack) 문제
"""

def solution(number, k):
    stack = []

    # 1. 숫자를 왼쪽부터 하나씩 순회한다.
    for num in number:

        # 2. 현재 숫자가 스택의 마지막 값보다 크고,
        #    아직 제거할 수 있는 횟수(k)가 남아있다면
        #    작은 숫자를 제거한다.
        while k > 0 and stack and stack[-1] < num:
            stack.pop()
            k -= 1

        # 3. 제거가 끝난 후 현재 숫자를 스택에 추가한다.
        stack.append(num)

    # 4. 순회가 끝난 후에도 k가 남아있다면
    #    뒤에서부터 k개를 제거한다.
    if k > 0:
        stack = stack[:-k]

    # 5. 스택에 남은 숫자들을 이어 붙여 문자열로 반환한다.
    return ''.join(stack)