"""
구해야 할 것:
장르별 총 재생 횟수가 높은 순서대로,
각 장르에서 재생 횟수가 많은 노래를 최대 2곡씩 선택하여
선택된 노래의 고유번호(인덱스)를 순서대로 반환하라.


---

1. 장르별 노래 정보와 총 재생 횟수를 딕셔너리에 저장한다.

2. 총 재생 횟수가 많은 장르부터 정렬한다.

3. 각 장르에서 재생 횟수가 많은 노래를 최대 2곡 선택한다.

4. 선택된 노래의 고유번호를 순서대로 반환한다.

"""


def solution(genres, plays):
    # 최종적으로 베스트 앨범에 포함될 노래의 고유번호(인덱스)를 저장할 리스트
    answer = []

    # 장르별 노래 정보 저장
    # key : 장르명
    # value : [(고유번호, 재생횟수), (고유번호, 재생횟수), ...]
    dic1 = {}

    # 장르별 총 재생 횟수 저장
    # key : 장르명
    # value : 해당 장르의 총 재생 횟수
    dic2 = {}

    # zip(genres, plays)는 장르와 재생 횟수를 묶어서 순회한다.
    # enumerate는 각 원소의 인덱스(i)를 함께 반환한다.
    for i, (g, p) in enumerate(zip(genres, plays)):

        # 현재 장르가 처음 등장한 경우
        if g not in dic1:
            # (노래 고유번호, 재생횟수)를 리스트에 저장
            dic1[g] = [(i, p)]
        else:
            # 이미 존재하는 장르라면 리스트에 추가
            dic1[g].append((i, p))

        # 장르별 총 재생 횟수 계산
        if g not in dic2:
            # 처음 등장한 장르라면 현재 재생 횟수로 초기화
            dic2[g] = p
        else:
            # 이미 존재하는 장르라면 재생 횟수를 누적
            dic2[g] += p

    # dic2.items()는 (장르, 총 재생횟수) 형태의 튜플을 반환한다.
    # sorted()를 이용해 총 재생 횟수가 많은 장르부터 내림차순 정렬한다.
    #
    # lambda x: x[1]
    # - 람다(lambda) 함수는 이름 없는(익명) 함수를 만드는 문법이다.
    # - 여기서 x는 (장르, 총 재생횟수) 튜플이다.
    # - x[1]은 튜플의 두 번째 값인 총 재생횟수를 의미한다.
    # - 즉, 총 재생횟수를 기준으로 정렬하기 위해 사용한다.
    for (k, v) in sorted(dic2.items(), key=lambda x: x[1], reverse=True):

        # 현재 장르(dic1[k])의 노래들을 재생 횟수 기준으로 내림차순 정렬한다.
        #
        # lambda x: x[1]
        # - x는 (고유번호, 재생횟수) 튜플이다.
        # - x[1]은 재생횟수이므로 재생횟수를 기준으로 정렬한다.
        #
        # [:2]
        # - 정렬된 결과 중 상위 2곡만 선택한다.
        for (i, p) in sorted(dic1[k], key=lambda x: x[1], reverse=True)[:2]:

            # 선택된 노래의 고유번호를 정답 리스트에 추가
            answer.append(i)

    # 최종 베스트 앨범의 노래 고유번호 목록 반환
    return answer
