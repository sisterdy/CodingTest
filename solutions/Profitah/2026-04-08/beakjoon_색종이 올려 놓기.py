"""
각 색종이는 가로와 세로 길이가 주어진다.
한 색종이를 다른 색종이 위에 올려놓으려면,
올려놓는 색종이의 가로와 세로가 모두 아래 색종이 이하여야 한다.
단, 색종이를 90도 회전시킬 수 있으며 변은 항상 평행을 유지해야 한다.
이때 최대 몇 장의 색종이를 쌓을 수 있는지 구한다.

색종이 목록 리스트를 순회하며 현재 인덱스 값과 이후 인덱스 값을 비교하고,
색종이를 쌓을 수 있는지 여부를 판별. 
이후 값을 갱신하며 탐색을 계속하면 될 듯하다.
DP알고리즘을 사용하자

또한 '최대' 몇장의 색종이를 쌓을 수 있는지 구해야하기에
LIS(최장수열)을 사용할 수 있다면 더 좋을 듯 하다.
---
최종 코드 흐름:
- 색종이 목록을 긴 변 기준 내림차순 정렬 후,
  i번 색종이를 맨 위로 가정하고 이전(j < i) 색종이들과 비교한다.
- 올려놓을 수 있으면 dp[i]를 갱신하며 최대 장수를 구한다.
"""

n = int(input())  # 색종이의 장수 입력
papers = []       # 색종이 목록

for i in range(n):
    w, h = map(int, input().split())
    papers.append((max(w, h), min(w, h)))  # (긴 변, 짧은 변) 으로 정규화
          
papers.sort(reverse=True)  # 긴 변 기준 내림차순 정렬 (종이를 큰 - 작은 순으로 나열하기 위해서는 가로와 세로 길이 모두를 비교하여야 하는데,
#가로와 세로 비교 과정을 1번으로 줄여 효율성 높이기 위함.)
                           
# dp[i] : i번째 색종이를 '맨 위'로 했을 때 쌓을 수 있는 최대 색종이 개수
# 처음에는 어떤 색종이든 자기 자신 1장은 가능하므로 dp배열의 상태값을 1로 초기화
dp = [1] * n  

# i번째 색종이를 맨 위에 놓는다고 가정하고 탐색
for i in range(n):

    # i보다 앞에 있는 색종이들을 확인
    # (정렬되어 있으므로 j번째 색종이는 항상 i번째보다 크거나 같음 - 긴 변 기준)
    # → 따라서 긴 변은 이미 조건 만족 상태
    # → 짧은 변만 비교하면 됨
    for j in range(i):

        # j번 색종이 위에 i번 색종이를 올릴 수 있는지 확인
        # (짧은 변도 j가 더 크거나 같아야 쌓을 수 있음)
        if papers[j][1] >= papers[i][1]:

            # j번 색종이까지 쌓은 개수에
            # i번 색종이를 하나 더 올리는 경우를 고려
            # 여러 j 중 가장 많이 쌓을 수 있는 경우를 선택
            dp[i] = max(dp[i], dp[j] + 1)

# 모든 경우 중 가장 많이 쌓은 개수가 정답
print(max(dp))


