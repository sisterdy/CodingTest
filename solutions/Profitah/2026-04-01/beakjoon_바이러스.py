"""
바이러스 감염 문제 - 1번 컴퓨터와 연결된 모든 컴퓨터의 수를 구하는 문제.

문제 설명:
- 컴퓨터 수 n, 연결 쌍 수 v가 주어지고, v개의 연결 정보가 입력된다.
- 1번 컴퓨터 감염 시, 연결된 모든 컴퓨터가 감염된다.
- 감염되는 컴퓨터 수를 출력한다. (1번 제외)

풀이 방식:
- 무방향 그래프를 인접 리스트로 구성한 뒤, 1번에서 DFS 탐색한다.
- 방문한 노드 수에서 1번을 제외(-1)하여 출력한다.

흐름:
- 그래프 구성 -> 1번에서 DFS -> 방문 수 합산 -> -1 후 출력
"""


n = int(input())  # 정점(컴퓨터)의 개수 입력
v = int(input())  # 간선(연결된 쌍)의 개수 입력

graph = [[] for i in range(n + 1)]  # 인접 리스트 초기화 (1번부터 n번까지 사용)
visited = [0] * (n + 1)  # 방문 여부를 기록하는 리스트 (0: 미방문, 1: 방문) // 과제 : truerfalse  sum으로 카운팅 되는지 알아오기 코파일럿 답변 : True/False로 표현된 리스트에서 sum() 함수를 사용하면, True는 1로, False는 0으로 계산되어 총합이 나옵니다. 따라서 방문 여부를 True/False로 표현해도 sum()을 통해 방문한 노드의 수를 쉽게 계산할 수 있습니다. 예를 들어, visited = [False, True, True, False] 라면 sum(visited) 는 2가 됩니다. 즉, 방문한 노드의 수가 2개임을 나타냅니다.

# 간선 정보 입력 받아서 그래프 구성
for i in range(v):
    a, b = map(int, input().split())  # 두 정점(컴퓨터) a, b 입력
    graph[a] += [b]  # a → b 연결
    graph[b] += [a]  # b → a 연결 (무방향 그래프이므로 양방향 추가)

# 깊이 우선 탐색 함수 정의
def dfs(v):
    visited[v] = 1  # 현재 노드 방문 처리
    for nx in graph[v]:  # 연결된 노드들에 대해
        if visited[nx] == 0:  # 아직 방문하지 않았다면
            dfs(nx)  # 그 노드로 DFS 계속 탐색

dfs(1)  # 1번 컴퓨터에서 탐색 시작

# 1번 컴퓨터를 통해 감염되는 컴퓨터 수 출력 
# 1번 컴퓨터는 감염 시작점으로, 카운팅 대상이 아니므로 -1
print(sum(visited) - 1)
