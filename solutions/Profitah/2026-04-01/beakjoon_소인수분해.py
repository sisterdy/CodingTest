"""
입력받은 수의 소인수를 순서대로 출력하는 문제.

정의:

- 소수(Prime): 1과 자기 자신만을 약수로 가지는 1보다 큰 자연수. (예: 2, 3, 5, 7, 11 ...)
- 소인수(Prime Factor): 어떤 수를 나누어 떨어지게 하는 소수.
  예) 12 = 2 × 2 × 3  →  소인수: 2, 2, 3

"""

num = int(input())  # 사용자 입력값 받기
i = 2               # 나눌 소인수 후보

while i <= num:         # i가 num 이하일 때
    if num % i == 0:    # i로 나누어 떨어지면
        print(i)        # 소인수 출력
        num = num // i  # num을 i로 나눈 몫으로 갱신
    else:
        i += 1          # 나누어 떨어지지 않으면 다음 수로