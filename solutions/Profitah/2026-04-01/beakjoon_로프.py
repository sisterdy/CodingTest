"""
로프 문제 - k개의 로프를 병렬로 사용할 때 들어올릴 수 있는 최대 중량을 구하는 문제.

문제 설명:
1. 로프 k개를 쓰면 무게가 k빵 되므로, 가장 약한 로프 × k = 들 수 있는 최대 중량
2. 내림차순 정렬하면 j번 인덱스 로프가 자동으로 가장 약한 로프가 됨
3. 모든 k에 대해 계산 후 최댓값 출력

풀이 방식:
- 로프를 내림차순 정렬하면, j번째 로프(0-indexed)까지 사용할 때 가장 약한 로프는 m[j]이다.
- 사용하는 로프 수는 (j+1)개이므로, 들 수 있는 중량 = m[j] × (j+1)
- 모든 경우를 계산한 후 최댓값을 출력한다.

흐름:
- 로프 중량 입력 -> 내림차순 정렬 -> 각 로프 수(k)별 최대 중량 계산 -> 최댓값 출력
"""

n = int(input())  # 로프의 개수 입력
m = []

for i in range(n):
    m.append(int(input()))  # 각 로프의 최대 중량을 한 줄씩 입력받아 추가

m.sort(reverse=True)  # 내림차순 정렬 (강한 로프가 앞으로)
                      # 정렬 후 j번째 로프가 가장 약한 로프가 됨

result = []

for j in range(n):
    result.append(m[j] * (j + 1))  # (가장 약한 로프 중량) × (사용하는 로프 수)
                                    # 예) m=[10,7,4], j=1 -> 7*2=14 (로프 2개 사용 시 최대 14)

print(max(result))  # 모든 경우 중 최대 중량 출력